#!/usr/bin/env bash
set -euo pipefail

template_url="${TEMPLATE_URL:-https://github.com/fastapi/full-stack-fastapi-template.git}"
template_ref="${TEMPLATE_REF:-master}"
target_dir="${1:-build/full-stack-fastapi-template-baseline}"

repo_root="$(git rev-parse --show-toplevel)"
cd "$repo_root"

mkdir -p "$(dirname "$target_dir")"
rm -rf "$target_dir"
git clone --depth 1 --branch "$template_ref" "$template_url" "$target_dir"

template_sha="$(git -C "$target_dir" rev-parse HEAD)"
evidence_dir="docs/evidence"
mkdir -p "$evidence_dir"

cat > "$evidence_dir/full_stack_fastapi_template_baseline.md" <<EOF
# Full Stack FastAPI Template Baseline

Generated by \`scripts/bootstrap_full_stack_template_baseline.sh\`.

## Source

- Repository: \`$template_url\`
- Ref: \`$template_ref\`
- Commit: \`$template_sha\`
- Local checkout: \`$target_dir\`

## Required Follow-up Evidence

- [ ] Docker Compose starts backend, frontend, and database.
- [ ] Backend tests pass.
- [ ] Frontend lint/build passes.
- [ ] Generated OpenAPI client can be regenerated.
- [ ] Playwright login smoke passes.
- [ ] OpenAPI docs are reachable.
- [ ] Known baseline failures are linked to follow-up issues.

EOF

printf 'Template baseline cloned to %s\n' "$target_dir"
printf 'Template commit: %s\n' "$template_sha"
printf 'Evidence stub: %s/full_stack_fastapi_template_baseline.md\n' "$evidence_dir"
